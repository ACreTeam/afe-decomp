static void aNSC_calc_talk_start_tim(NPC_SHOP_COMMON_ACTOR* shop_common) {
    int ivar1 = shop_common->talk_start_tim;
    if (ivar1 <= -1) {
        ivar1 = 180;
    } else if (ivar1 != 0) {
        ivar1 -= 1;
    }
    shop_common->talk_start_tim = ivar1;
}

