static void aNSC_sell_camera(NPC_SHOP_COMMON_ACTOR* shop_common, GAME_PLAY* play) {
    if (shop_common->action >= aNSC_ACTION_SELL_CHECK_BEFORE && shop_common->action <= aNSC_ACTION_CHG_CLOTH_END_WAIT) {
        switch (ITEM_NAME_GET_TYPE(shop_common->sell_item)) {
            case NAME_TYPE_FTR0:
            case NAME_TYPE_FTR1:
                aNSC_set_sell_camera(shop_common, play);
                break;
            case NAME_TYPE_ITEM1:
                switch (ITEM_NAME_GET_CAT(shop_common->sell_item)) {
                    case ITEM1_CAT_CLOTH:
                        break;
                    case ITEM1_CAT_TOOL:
                        if (ITEM_IS_UMBRELLA2(shop_common->sell_item)) {
                            break;
                        }
                        // fall through
                    default:
                        return;
                }
                aNSC_set_sell_camera(shop_common, play);
                break;
        }
    }
}

