static void aSOI_ins_add_range_info_island(aSOI_insect_keep_c* keep, aSOI_insect_spawn_info_f_c* spawn_info, u8* spawn_info_num, aSetMgr_player_pos_c* player_pos) {
    static aSOI_insect_spawn_info_f_c additional_data[2] = {
        // clang-format off
        INSECT_SPAWN(ANT, ON_CANDY, 1.0f),
        INSECT_SPAWN(ANT, ON_TRASH, 1.0f),
        // clang-format on
    };

    static aSOI_insect_spawn_info_f_c island_goki_additional_data = INSECT_SPAWN(COCKROACH, ISLAND_GOKI, 1.0f);
    
    int island_bx[mISL_FG_BLOCK_X_NUM];
    int idx;
    int trash_count;
    int i;

    for (i = 0; i < ARRAY_COUNT(additional_data); i++) {
        bcopy(&additional_data[i], spawn_info, sizeof(aSOI_insect_spawn_info_f_c));
        spawn_info++;
    }

    (*spawn_info_num) += ARRAY_COUNT(additional_data);

    mFI_GetIslandBlockNumX(island_bx);
    idx = player_pos->next_bx - island_bx[0];
    trash_count = aSOI_count_trash_in_island(idx);
    if (trash_count > 0) {
        bcopy(&island_goki_additional_data, spawn_info, sizeof(aSOI_insect_spawn_info_f_c));
        spawn_info->weight = trash_count * 5.0f;
        (*spawn_info_num) += 1;
    }

    keep->island_trash_num[idx] = trash_count;
}

