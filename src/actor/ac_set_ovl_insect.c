static int aSOI_ins_field_under_a_tree_check_get(mActor_name_t* fg_p) {
    int ret = FALSE;

    if (fg_p != NULL) {
        int uz;
        int ux;

        for (uz = 0; uz < UT_Z_NUM; uz++) {
            for (ux = 0; ux < UT_X_NUM; ux++) {
                if (uz >= 2 && uz < (UT_Z_NUM - 2) && ux >= 2 && ux < (UT_X_NUM - 2)) {
                    if (aSOI_GOKI_TREE_CHK(fg_p[UT_X_NUM]) && *fg_p == EMPTY_NO) {
                        ret = TRUE;
                        break;
                    }
                }

                fg_p++;
            }

            if (ret == TRUE) {
                break;
            }
        }
    }

    return ret;
}

static int aSOI_ins_field_island_goki_check_get(mActor_name_t* fg_p, u16* deposit_p) {
    int ret;

    ret = aSOI_ins_field_tree_check_get(fg_p);
    if (ret == FALSE) {
        ret = aSOI_ins_field_fg_check_get(fg_p, deposit_p, TRUE, FLOWER_PANSIES0, FLOWER_COSMOS2);
    }

    if (ret == FALSE) {
        ret = aSOI_ins_field_fg_check_get(fg_p, deposit_p, FALSE, ITM_DUST0_EMPTY_CAN, ITM_DUST2_OLD_TIRE);
    }

    if (ret == FALSE) {
        ret = aSOI_ins_field_fg_check_get(fg_p, deposit_p, FALSE, ITM_KABU_SPOILED, ITM_KABU_SPOILED);
    }

    if (ret == FALSE) {
        ret = aSOI_ins_field_under_a_tree_check_get(fg_p);
    }

    return ret;
}

