static void aSD_CleanReserve(void) {
    mActor_name_t* fg_p = mFI_BkNumtoUtFGTop(0, 0);
    int i;

    for (i = 0; i < UT_TOTAL_NUM; i++) {
        if (ITEM_IS_RSV_SHOP(fg_p[i])) {
            fg_p[i] = EMPTY_NO;
        }
    }
}

static void aSD_SetReserveFG(mActor_name_t* reserve_list_p) {
    mActor_name_t* fg_p = mFI_BkNumtoUtFGTop(0, 0);
    int i;

    for (i = 0; i < UT_TOTAL_NUM; i++) {
        if (ITEM_IS_RSV_SHOP(reserve_list_p[i])) {
            fg_p[i] = reserve_list_p[i];
        }
    }
}

static void aSD_ChangeSpecialLayout(int scene_no) {
    int status = Common_Get(tanuki_shop_status);
    int layout_idx = -1;
    int layout_type;

    switch (scene_no) {
        case SCENE_SHOP0:
            layout_idx = aSD_LAYOUT_IDX_SHOP0;
            break;
        case SCENE_CONVENI:
            layout_idx = aSD_LAYOUT_IDX_CONVENI;
            break;
        case SCENE_SUPER:
            layout_idx = aSD_LAYOUT_IDX_SUPER;
            break;
        case SCENE_DEPART:
            layout_idx = aSD_LAYOUT_IDX_DEPART;
            break;
        case SCENE_DEPART_2:
            layout_idx = aSD_LAYOUT_IDX_DEPART_2;
            break;
    }

    switch (status) {
        case mSP_TANUKI_SHOP_STATUS_EVENT:
            layout_type = aSD_LAYOUT_TYPE_SPECIAL;
            break;
        case mSP_TANUKI_SHOP_STATUS_FUKUBIKI:
            if (mSP_force_opend()) {
                layout_type = aSD_LAYOUT_TYPE_NOMRAL;
            } else {
                layout_type = -1;
            }
            break;
        case mSP_TANUKI_SHOP_STATUS_HUKUBUKURO_SALE:
            layout_type = aSD_LAYOUT_TYPE_HUKUBUKURO;
            break;
        default:
            layout_type = aSD_LAYOUT_TYPE_NOMRAL;
            break;
    }

    if (layout_idx != -1 && layout_type != -1) {
        static mActor_name_t* aSD_special_layout_table[aSD_LAYOUT_IDX_NUM] = {
            aSD_shop_special_layout, // aSD_LAYOUT_IDX_SHOP0
            aSD_conveni_special_layout, // aSD_LAYOUT_IDX_CONVENI
            aSD_super_special_layout, // aSD_LAYOUT_IDX_SUPER
            aSD_depart1_special_layout, // aSD_LAYOUT_IDX_DEPART
            aSD_depart2_special_layout, // aSD_LAYOUT_IDX_DEPART_2
        };
        
        static mActor_name_t* aSD_hukubukuro_layout_table[aSD_LAYOUT_IDX_NUM] = {
            aSD_shop_hukubukuro_layout, // aSD_LAYOUT_IDX_SHOP0
            aSD_conveni_hukubukuro_layout, // aSD_LAYOUT_IDX_CONVENI
            aSD_super_hukubukuro_layout, // aSD_LAYOUT_IDX_SUPER
            aSD_depart1_hukubukuro_layout, // aSD_LAYOUT_IDX_DEPART
            aSD_depart2_normal_layout, // aSD_LAYOUT_IDX_DEPART_2
        };
        
        static mActor_name_t* aSD_normal_layout_table[aSD_LAYOUT_IDX_NUM] = {
            aSD_shop_normal_layout, // aSD_LAYOUT_IDX_SHOP0
            aSD_conveni_normal_layout, // aSD_LAYOUT_IDX_CONVENI
            aSD_super_normal_layout, // aSD_LAYOUT_IDX_SUPER
            aSD_depart1_normal_layout, // aSD_LAYOUT_IDX_DEPART
            aSD_depart2_normal_layout, // aSD_LAYOUT_IDX_DEPART_2
        };
        
        static mActor_name_t** aSD_shop_layput_table[aSD_LAYOUT_TYPE_NUM] = {
            aSD_normal_layout_table, // aSD_LAYOUT_TYPE_NOMRAL
            aSD_special_layout_table, // aSD_LAYOUT_TYPE_SPECIAL
            aSD_hukubukuro_layout_table, // aSD_LAYOUT_TYPE_HUKUBUKURO
        };
        
        mActor_name_t** layout_table = aSD_shop_layput_table[layout_type];

        if (layout_table[layout_idx] != NULL) {
            aSD_CleanReserve();
            aSD_SetReserveFG(layout_table[layout_idx]);
        }
    }
}

