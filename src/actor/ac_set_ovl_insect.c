/* sizeof(aSOI_term_info_c) == 0xC */
typedef struct term_info_s {
    /* 0x00 */ int type; /* insect type (aINS_INSECT_TYPE_*) */
    /* 0x04 */ u8 spawn_area; /* spawn area (aSOI_SPAWN_AREA_*) */
    /* 0x05 */ u8 weight; /* weight value */
} aSOI_term_info_c;

/* sizeof (aSOI_term_list_c) == 8 */
