static mActor_name_t aSD_UnitNum2ItemNo(int ux, int uz) {
    int bx = ux / UT_X_NUM;
    int bz = uz / UT_Z_NUM;
    mActor_name_t* fg_p = mFI_BkNumtoUtFGTop(bx, bz);
    int local_ux = ux - bx * UT_X_NUM;
    int local_uz = uz - bz * UT_Z_NUM;
    mActor_name_t item = fg_p[local_ux + local_uz * UT_X_NUM];

    if (ITEM_IS_RSV_SHOP_SOLDOUT(item)) {
        return RSV_NO;
    }

    if (
        // clang-format off
        ITEM_IS_FTR(item) ||
        (item >= ITM_PAPER_START && item <= ITM_PAPER_END) ||
        (item >= ITM_CARPET_START && item <= ITM_CARPET_END - 4) ||
        (item >= ITM_WALL_START && item <= ITM_WALL_END - 4) ||
        (item >= ITM_CLOTH_START && item <= ITM_CLOTH_END) ||
        (item >= ITM_TOOL_START && item <= ITM_TOOL_END) ||
        (item >= ITM_SAPLING && item <= ITM_CEDAR_SAPLING) ||
        ITEM_IS_FLOWER_BAG(item) ||
        (item >= ITM_DIARY00 && item <= ITM_DIARY15) ||
        (item == ITM_FOOD_CANDY) ||
        (item == RSV_FE1F) ||
        (item == ITM_HUKUBUKURO_BAG) ||
        (item == ITM_SIGNBOARD) ||
        (item == ITM_MEDICINE)
        // clang-format on
    ) {
        if (item == RSV_FE1F) {
            if (aMR_CLIP != NULL) {
                mActor_name_t ftr_item;

                aMR_CLIP->unitNum2ftrItemNoftrId_proc(&ftr_item, NULL, ux, uz, mCoBG_LAYER0);
                return ftr_item;
            } else {
                return RSV_NO;
            }
        } else {
            return item;
        }
    } else {
        return RSV_NO;
    }
}

static int aSD_ReportGoodsSales(int ux, int uz) {
    mActor_name_t item = aSD_UnitNum2ItemNo(ux, uz);
    u32 price = mSP_ItemNo2ItemPrice_Tanu(item);
    mActor_name_t rsv_item = EMPTY_NO;
    mActor_name_t event_rsv_item = EMPTY_NO;
    mActor_name_t ftr_item;
    int tanuki_shop_status = Common_Get(tanuki_shop_status);
    int ftrID;

    if (CLIP(shop_design_clip) != NULL) {
        SHOP_DESIGN_ACTOR* shop_design = CLIP(shop_design_clip)->design_actor;

        mSP_PlusSales(price);
        
        if (item == ITM_HUKUBUKURO_BAG || item == ITM_CRACKER) {
            event_rsv_item = RSV_SHOP_SOLD_FE16;
        }
        
        if (item >= ITM_YELLOW_PINWHEEL && item <= ITM_FANCY_PINWHEEL) {
            event_rsv_item = RSV_SHOP_SOLD_PAINT;
        }
        
        if (item >= ITM_BLUEBELL_FAN && item <= ITM_LEAF_FAN) {
            event_rsv_item = RSV_SHOP_SOLD_TOOL;
        }

        if (event_rsv_item != EMPTY_NO) {
            mSP_ShopSaleReport(item, shop_design->goods_list, shop_design->goods_list_max, event_rsv_item);
            mFI_UtNumtoFGSet_common(event_rsv_item, ux, uz, FALSE);
            return FALSE;
        }

        if (item >= ITM_SAPLING && item <= ITM_CEDAR_SAPLING) {
            rsv_item = RSV_SHOP_SOLD_PLANT;
        }

        if (ITEM_IS_FLOWER_BAG(item)) {
            if (tanuki_shop_status == mSP_TANUKI_SHOP_STATUS_HALLOWEEN || tanuki_shop_status == mSP_TANUKI_SHOP_STATUS_CRACKER) {
                rsv_item = RSV_SHOP_SOLD_PLANT;
            } else {
                rsv_item = RSV_SHOP_SOLD_FE16;
            }
        }

        if (item == ITM_FOOD_CANDY) {
            rsv_item = RSV_SHOP_SOLD_FE16;
        }

        if (item >= ITM_CLOTH_START && item <= ITM_CLOTH_END) {
            mSP_ShopSaleReport(item, shop_design->goods_list, shop_design->goods_list_max, RSV_SHOP_SOLD_CLOTH);
            CLIP(shop_manekin_clip)->change2naked_manekin_proc(ux, uz);
            mFI_UtNumtoFGSet_common(RSV_NO, ux, uz, FALSE);
            return FALSE;
        }

        if (ITEM_IS_FTR(item) && aMR_CLIP != NULL) {
            aMR_CLIP->unitNum2ftrItemNoftrId_proc(&ftr_item, &ftrID, ux, uz, mCoBG_LAYER0);
            aMR_CLIP->ftrId2extinguishFtr_proc(ftrID);

            if (mNT_ftr_item_no_to_ftr_idx(item) >= FTR_NOG_BALLOON_COMMON0 && mNT_ftr_item_no_to_ftr_idx(item) <= FTR_NOG_BALLOON_COMMON7) {
                rsv_item = RSV_SHOP_SOLD_PLANT;
            } else if (item == Save_Get(shop).rare_item && tanuki_shop_status != mSP_TANUKI_SHOP_STATUS_EVENT) {
                rsv_item = RSV_SHOP_SOLD_RARE;
            } else {
                rsv_item = RSV_SHOP_SOLD_FTR;
            }
        }

        if (ITEM_IS_REG_CARPET(item)) {
            rsv_item = RSV_SHOP_SOLD_CARPET;
        }

        if (ITEM_IS_REG_WALL(item)) {
            rsv_item = RSV_SHOP_SOLD_WALL;
        }

        if (item == ITM_SIGNBOARD) {
            rsv_item = RSV_SHOP_SOLD_SIGNBOARD;
        }

        if (item == ITM_MEDICINE) {
            rsv_item = RSV_SHOP_SOLD_MEDICINE;
        } else if (item >= ITM_TOOL_START && item <= ITM_TOOL_END) {
            if (ITEM_IS_UMBRELLA2(item)) {
                if (CLIP(shop_umbrella_clip) != NULL) {
                    CLIP(shop_umbrella_clip)->delete_umbrella_proc(ux, uz);
                }

                rsv_item = RSV_SHOP_SOLD_UMBRELLA;
            } else if (ITEM_IS_PAINT(item)) {
                rsv_item = RSV_SHOP_SOLD_PAINT;
            } else if (item == ITM_SIGNBOARD) {
                rsv_item = RSV_SHOP_SOLD_SIGNBOARD;
            } else {
                rsv_item = RSV_SHOP_SOLD_TOOL;
            }
        }

        if (item >= ITM_PAPER_START && item <= ITM_PAPER_END) {
            return TRUE;
        }

        if (item >= ITM_DIARY00 && item <= ITM_DIARY15) {
            rsv_item = RSV_SHOP_SOLD_PAPER;
        }

        if (tanuki_shop_status == mSP_TANUKI_SHOP_STATUS_EVENT && !mEv_CheckEvent(mEv_SPNPC_SHOP)) {
            return FALSE;
        }

        if (rsv_item != EMPTY_NO) {
            if (mSP_ShopSaleReport(item, shop_design->goods_list, shop_design->goods_list_max, rsv_item)) {
                return TRUE;
            }

            mFI_UtNumtoFGSet_common(rsv_item, ux, uz, FALSE);
        }
        
        return FALSE;
    }

    return FALSE;
}
