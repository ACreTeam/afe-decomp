static int aSOI_check_live_ut_island_goki(mActor_name_t* fg_p) {
    int ret = FALSE;

    if (aSOI_tree_check(*fg_p)) {
        ret = TRUE;
    } else if (IS_ITEM_BUG_FLOWER(*fg_p)) {
        ret = TRUE;
    } else if (ITEM_IS_DUST(*fg_p) || *fg_p == ITM_KABU_SPOILED) {
        ret = TRUE;
    } else if (aSOI_GOKI_TREE_CHK(fg_p[UT_X_NUM]) && *fg_p == EMPTY_NO) {
        ret = TRUE;
    }

    return ret;
}

static int aSOI_check_live_ut_free(mActor_name_t* fg_p) {
    int ret = FALSE;

    if (*fg_p == EMPTY_NO || *fg_p == RSV_NO) {
        ret = TRUE;
    } else {
        switch (ITEM_NAME_GET_TYPE(*fg_p)) {
            case NAME_TYPE_FTR0:
            case NAME_TYPE_FTR1:
            case NAME_TYPE_ITEM1:
                ret = TRUE;
                break;
        }
    }

    return ret;
}

