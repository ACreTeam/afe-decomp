static int mMck_strlen2(u16* str, int len) {
    u16* end_p = str + len;

    while (len != 0) {
        len--;

        if (*end_p != CHAR_SPACE) {
            goto end; /* Don't like this */
        }

        end_p--;
    }

    len--;

end:
    return len + 1;
}

static int mMck_cmp_sep(u16 c) {
    switch (c) {
        case CHAR_PP_032:
        case CHAR_PP_033:
        case CHAR_PP_044:
        case CHAR_PP_046:
        case CHAR_PP_063:
        case CHAR_PP_129:
        case CHAR_PP_132:
        case CHAR_PP_133:
        case CHAR_PP_194:
        case CHAR_PP_205:
            return TRUE;

        default:
            return FALSE;
    }
}

static u16* mMck_search_sep(u16* str, u8 len) {
    while (len-- != 0) {

        /* Only accept the separator if the next character is also not a separator */
        if (mMck_cmp_sep(str[0]) == TRUE && mMck_cmp_sep(str[1]) == FALSE) {
            break;
        }

        str++;
    }

    return str;
}

static u8* mMck_next_key(u8* str) {
    return str + 2;
}

